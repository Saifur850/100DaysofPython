year= float(input("Which year do you want to check? "))

if year%4!=0:
    print(f"The year {year} is not a leap year! ")
else:
    if year%100!=0:
        print(f"The year {year} is not a leap year! ")
    else:
        if year%100!=0:
           print(f"The year {year} is not a leap year! ")
        else:
           print(f"The year {year} is a leap year! ")

